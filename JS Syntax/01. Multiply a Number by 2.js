function multiplyNumbers(number) {
    let finalNumber = parseInt(number);
    return finalNumber * 2;
}

let numbers = ['2', '3', '30', '13'];

console.log(multiplyNumbers(numbers[0]));
console.log(multiplyNumbers(numbers[1]));
console.log(multiplyNumbers(numbers[2]));
console.log(multiplyNumbers(numbers[3]));
