function multiplyNumbers(number1, number2) {
    return parseInt(number1) * parseInt(number2);
}

let numbers = ['2', '3', '13', '13', '1', '2', '0', '50'];

console.log(multiplyNumbers(numbers[0], numbers[1]));
console.log(multiplyNumbers(numbers[2], numbers[3]));
console.log(multiplyNumbers(numbers[4], numbers[5]));
console.log(multiplyNumbers(numbers[6], numbers[7]));